    /**
     * iterator()
     * hasNext()
     * next()
     * remove()
     */

    @Test
    public void learn_Iterator() {
        List<String> list = new CopyOnWriteArrayList<>();
        list.add("Apple");
        list.add("Banana");
        list.add("Orange");

        //Created an iterator
        Iterator<String> itr = list.iterator();
        boolean hasNext = itr.hasNext();
        while (hasNext) {
            System.out.println(itr.next());
        }
       itr.remove();
       System.out.println(itr.next());
    }
