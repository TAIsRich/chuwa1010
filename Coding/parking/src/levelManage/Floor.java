package levelManage;

public class Floor {
    private int floorNum;
    private int size;
    private int aviSpot;

    public int getFloorNum() {
        return floorNum;
    }

    public void setFloorNum(int floorNum) {
        this.floorNum = floorNum;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public int getAviSpot() {
        return aviSpot;
    }

    public void setAviSpot(int aviSpot) {
        this.aviSpot = aviSpot;
    }
}
